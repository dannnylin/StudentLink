import UIKit

extension UIColor {
    class func nicePurple() -> UIColor {
        return UIColor(red: 49/255, green: 27/255, blue: 146/255, alpha: 1)
    }
    
    class func facebook() -> UIColor {
        return UIColor(red: 59/255, green: 89/255, blue: 152/255, alpha: 1)
    }
}